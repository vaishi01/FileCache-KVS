#pragma once

/**
 * This file defines constants and types that are used across this project.
 */

/**
 * Return `SUCCESS` upon successful completion.
 */
#define SUCCESS 0

/**
 * Return `FAILURE` when something goes wrong.
 */
#define FAILURE 1

/**
 * `KVS_KEY_MAX` is the maximum length of the key (including the null
 * terminator).
 */
#define KVS_KEY_MAX 128

/**
 * `KVS_VALUE_MAX` is the maximum length of the value (including the null
 * terminator).
 */
#define KVS_VALUE_MAX 512
